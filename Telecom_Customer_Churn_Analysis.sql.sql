-- 1. Overall churn rate.
-- =========================================
-- Columns used: Churn,CustomerID
-- Query:
   SELECT 
       SUM(CASE WHEN Churn = 'Yes' THEN 1 ELSE 0 END) * 1.0 / COUNT(*) AS churn_rate
   FROM customer_churn;
