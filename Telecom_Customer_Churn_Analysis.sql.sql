-- ==========================================
-- 4. Customers into value segments based on tenure and revenue.
-- ==========================================
-- Columns used: CustomerID,Tenure,TotalCharges
-- Query:
   SELECT CustomerID,
          Tenure,
          TotalCharges AS TotalCharges,
 
        CASE
             WHEN Tenure >= 48
                  AND TotalCharges >= 4000
             THEN 'High Value'
 
             WHEN Tenure >= 24
                  AND TotalCharges >= 2000
             THEN 'Medium Value'
              
             ELSE 'Low Value'
        END AS customer_segment
 
   FROM customer_churn;
-- ==========================================

-- ==========================================
-- 5. Customers into churn-risk segments based on tenure, contract type, and monthly charges.
-- ==========================================
-- Columns used: CustomerID,Tenure,Contract,MonthlyCharges
-- Query:
   SELECT CustomerID,
 
        CASE
     WHEN Contract='Month-to-month'
          AND Tenure < 12
          AND MonthlyCharges > 80
     THEN 'High Risk'
      
     WHEN Contract='Month-to-month'
          AND Tenure BETWEEN 12 AND 24
     THEN 'Medium Risk'
      
     WHEN Contract='One year'
     THEN 'Medium Risk'
      
     ELSE 'Low Risk'
     END AS churn_risk
      
    FROM customer_churn;
-- ==========================================

