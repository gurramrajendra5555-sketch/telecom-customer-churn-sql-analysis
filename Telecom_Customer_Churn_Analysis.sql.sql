    SELECT CustomerID,
        Contract,
        TotalCharges AS TotalCharges
 
    FROM customer_churn c1
 
    WHERE TotalCharges
    >
   (
   SELECT AVG(TotalCharges)
   FROM customer_churn c2
   WHERE c1.Contract = c2.Contract
   );
