-- ==========================================
-- 10. Customer segments that generate high revenue but also experience high churn.
-- ==========================================
-- Columns used: Contract,Churn,TotalCharges
-- Query:
SELECT
    CASE
        WHEN TotalCharges >= 5000 THEN 'High Revenue'
        WHEN TotalCharges >= 2000 THEN 'Medium Revenue'
        ELSE 'Low Revenue'
    END AS revenue_segment,

    COUNT(*) AS total_customers,

    ROUND(
        SUM(CASE WHEN Churn = 'Yes' THEN 1 ELSE 0 END) * 100.0 / COUNT(*),
        2
    ) AS churn_rate,

    ROUND(SUM(TotalCharges), 2) AS total_revenue

FROM customer_churn

GROUP BY
    CASE
        WHEN TotalCharges >= 5000 THEN 'High Revenue'
        WHEN TotalCharges >= 2000 THEN 'Medium Revenue'
        ELSE 'Low Revenue'
    END

ORDER BY churn_rate DESC;
