-- 2. Customer retention rate.
-- =========================================
-- Columns used: Churn,CustomerID
-- Query:
   SELECT 
     (SUM(CASE WHEN Churn = 'No' THEN 1 ELSE 0 END) * 1.0 / COUNT(CustomerID)) AS retention_rate
   FROM customer_churn;
